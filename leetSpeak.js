
function hacker_speak(test) {
    i = 0;
    while (i <= test.length) { 
        i++;                        //iterate through string                                 
        if (i = 'A') {               //conditional statements that indicate which letters to replace with which numbers    
            console.log("4");
        }  
        else if (i = 'E') {
            console.log("3" + '');
        } 
        else if (i = 'G') {
            console.log("6");
        }
        else if (i = 'I') {
            console.log("1");
        }
        else if (i = 'O') {
            console.log("0");
        }
        else if (i = 'S') {
            console.log("5");
        }
        else if (i = 'T') {
            console.log("7");
        }  
        // else {
        //     console.log(i);
        // }
    }   
}
